function levenshtein(a: string, b: string): number {
  const m = a.length, n = b.length;
  const dp: number[][] = Array.from({ length: m + 1 }, (_, i) =>
    Array.from({ length: n + 1 }, (__, j) => (i === 0 ? j : j === 0 ? i : 0))
  );
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      dp[i][j] = a[i - 1] === b[j - 1]
        ? dp[i - 1][j - 1]
        : 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
    }
  }
  return dp[m][n];
}

// Returns true if the answer is off by exactly 1 character from any accepted value.
export function isNearMiss(answer: string, accepted: string[]): boolean {
  const norm = answer.trim().toLowerCase();
  if (norm.length < 3) return false; // too short to forgive typos
  return accepted.some((a) => levenshtein(norm, a.toLowerCase()) === 1);
}
